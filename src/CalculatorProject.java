    private double firstNumber;
    private double result;
    private String operations;
    private boolean isNewNumber = true;
    
    // ================================[ Result ]===============================
    private void calculateResult() {
        double secondNumber = Double.parseDouble(jTextArea1.getText());
        
        switch (operations) {
            case "+":
                result = firstNumber + secondNumber;
                break;
            case "-":
                result = firstNumber - secondNumber;
                break;
            case "x":
                result = firstNumber * secondNumber;
                break;
            case "/":
                if (secondNumber != 0) {
                    result = firstNumber / secondNumber;
                } else {
                    jTextArea1.setText("Error");
                    return;
                }
                break;
        }
        
        // Format result to avoid unnecessary decimal places
        String resultText = (result == (long) result) 
            ? String.format("%d", (long) result) 
            : String.format("%.2f", result);
        
        jTextArea1.setText(resultText);
    }
