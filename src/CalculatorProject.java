    // ====================[ Click on number button ]===========================
    private void numberButtonClicked(String number) {
        if (isNewNumber) {
            jTextArea1.setText(number);
            isNewNumber = false;
        } else {
            if (jTextArea1.getText().equals("0")) {
                jTextArea1.setText(number);
            } else {
                jTextArea1.setText(jTextArea1.getText() + number);
            }
        }
    }
    // =========================================================================
    
    // ===================[ Click on operator button ]==========================
    private void operationButtonClicked(String operation) {
        try {
            firstNumber = Double.parseDouble(jTextArea1.getText());
            operations = operation;
            jTextArea1.setText(jTextArea1.getText() + " " + operation);
            isNewNumber = true;
        } catch (NumberFormatException e) {
            jTextArea1.setText("Error");
        }
    }
